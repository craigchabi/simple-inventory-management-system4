    private void loadTable() {
        try {
            OrderController oc = new OrderController();
            CustomerController cc = new CustomerController();
            ResultSet r = oc.loadOrders();

            model = (DefaultTableModel) tblOrder.getModel();
            model.setRowCount(0);
            while (r.next()) {
                Vector v = new Vector();
                v.add(r.getString("randId"));
                v.add(cc.getCutomerNameById(Integer.parseInt(r.getString("customerId"))).equals("") ? "Deleted Customer" : cc.getCutomerNameById(Integer.parseInt(r.getString("customerId"))));
                v.add(r.getString("date"));
                v.add(r.getFloat("price"));
                model.addRow(v);
            }

            tblOrder.setModel(model);

        } catch (Exception e) {
        }

    }

    private void searchOrder(String data) {

        try {
            OrderController oc = new OrderController();
            CustomerController cc = new CustomerController();
            ResultSet r = oc.searchOrdersByCustomerName(data);

            model = (DefaultTableModel) tblOrder.getModel();
            model.setRowCount(0);
            while (r.next()) {
                Vector v = new Vector();
                v.add(r.getString("randId"));
                v.add(cc.getCutomerNameById(Integer.parseInt(r.getString("customerId"))));
                v.add(r.getString("date"));
                v.add(r.getFloat("price"));
                model.addRow(v);
            }

            tblOrder.setModel(model);

        } catch (Exception e) {
        }

    }

