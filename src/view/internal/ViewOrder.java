    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        OrderItemController oic = new OrderItemController();
        ItemController ic = new ItemController();
        String or = "", d = "", tot = "";

        try {

            if (!selectedOr.equals("")) {
                or = selectedOr;
                int row = tblOrder.getSelectedRow();
                d = tblOrder.getModel().getValueAt(row, 2).toString();
                tot = tblOrder.getModel().getValueAt(row, 3).toString();
                Tools tool = new Tools();
                String strhtml = "<html>"
                        + "<head><meta name='viewport' content='width=device-width', initial-scale=1.0'>"
                        + "<style>"
                        + "body{"
                        + "font-family:arial;"
                        + "}"
                        + ".rhead{background-color:#001e6c; }"
                        + ".col-head{background-color:#035397;}"
                        + ".wh{color:#ffffff;}"
                        
                        + "</style>"
                        + "</head>"
                        + "<body>"
                        + "<table style='width:100%'>"
                        + "<tr class='rhead'><th colspan='3'><h1 class='wh' >INVOICE</h1></th><tr>"
                        + "<tr><td>Order Ref : " + or + "</td><td></td><td>Date :" + d
                        + "</td></tr></table><br><br>"
                        + "<table style='width:100%; font-size:13px;' >"                                              
                        + "<tr class='col-head'><td class='wh' ><b>Item Code</b></td>"
                        + "<td class='wh'><b>Quantity</b></td>"
                        + "<td class='wh'><b>Price</b></td></tr>";

                ResultSet r = oic.getOrderItemsByRef(or);

                while (r.next()) {
                    float p = ic.getItemPrice(r.getString("itemCode")) * r.getInt("quantity");
                    strhtml = strhtml + "<tr><td>"
                            + r.getString("itemCode") + "</td><td>"
                            + r.getInt("quantity") + "</td><td>"
                            + p + "</td></tr>";

                }

                strhtml = strhtml + "<tr><td></td><td></td><td style='color:blue;' >" + tot + " </td></tr></table></body></html>";

                HtmlReport hr = new HtmlReport((Frame) tool.getVisibleContent(), true);
                hr.setLocationRelativeTo((Frame) tool.getVisibleContent());
                hr.loadHtml(strhtml);
                hr.setVisible(true);

            }

        } catch (Exception e) {
        }


    }//GEN-LAST:event_btnViewActionPerformed


